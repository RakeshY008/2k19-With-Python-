x = float (input("Enter length of rectangle : "))
y = float (input("Enter width of rectangle : "))

	# calculation
area = (x * y)
perimeter = 2*(x + y)

print(" Perimeter of rectangle = %.2f" %(perimeter))
print(" Area of rectangle = %.2f" %(area))



